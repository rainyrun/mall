package top.rainyrun.mall.dao.pojo;

import java.io.Serializable;
import java.util.Date;

public class MallContentCat implements Serializable {
    private Long id;

    private Long parentId;

    private String name;

    private Integer sortOrder;

    private Boolean isParent;

    private Date created;

    private Date updated;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getParentId() {
        return parentId;
    }

    public void setParentId(Long parentId) {
        this.parentId = parentId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name == null ? null : name.trim();
    }

    public Integer getSortOrder() {
        return sortOrder;
    }

    public void setSortOrder(Integer sortOrder) {
        this.sortOrder = sortOrder;
    }

    public Boolean getIsParent() {
        return isParent;
    }

    public void setIsParent(Boolean isParent) {
        this.isParent = isParent;
    }

    public Date getCreated() {
        return created;
    }

    public void setCreated(Date created) {
        this.created = created;
    }

    public Date getUpdated() {
        return updated;
    }

    public void setUpdated(Date updated) {
        this.updated = updated;
    }

	@Override
	public String toString() {
		return "MallContentCat [id=" + id + ", parentId=" + parentId + ", name=" + name + ", sortOrder=" + sortOrder
				+ ", isParent=" + isParent + ", created=" + created + ", updated=" + updated + "]";
	}
    
}